               <h3 >1.Eat a variety of foods</h3>
               <br>
               <img src="<?php echo base_url('image/variety of foods.png')?>" width="400px" height="200px">
               <br>
               <br>
               <p class="details">For good health, we need more than 40 different nutrients, and no 
                 single food can supply them all. It is not about a single meal, it 
                 is about a balanced food choice over time that will make a difference!
               <ul class="list">
                 <li>A high-fat lunch could be followed by a low-fat dinner.</li>
                 <li>After a large meat portion at dinner, perhaps fish should be the next day’s choice?</li>
               </ul>
               </p>

               <!--2 nd-->
               <p>
                <a name="2"></a>
               </p>
               <h3 >2.Base your diet on plenty of foods rich in carbohydrates</h3>
               <br>
               <img src="<?php echo base_url('image/whats-in-food-carbohydrates-eufic.png')?>" width="400px" height="200px">
               <br>
               <br>
               <p class="details">About half the calories in our diet should come from foods rich in carbohydrates,
                 such as cereals, rice, pasta, potatoes, and bread. It is a good idea to include 
                 at least one of these at every meal. Wholegrain foods, like wholegrain bread, 
                 pasta, and cereals, will increase our fibre intake.
               </p>

               <!--3 rd-->
               <p>
                <a name="3"></a>
               </p>
               <h3 >3.Replace saturated with unsaturated fat</h3>
               <br>
               <img src="<?php echo base_url('image/images.jfif')?>" width="400px" height="200px">
               <br>
               <br>
               <p class="details">Fats are important for good health and proper functioning of the body. 
                   However, too much of it can negatively affect our weight and cardiovascular health. 
                   Different kinds of fats have different health effects, and some of these tips 
                   could help us keep the balance right:
                 <ul class="list">
                     <li>We should limit the consumption of total and saturated fats (often coming 
                         from foods of animal origin), and completely avoid trans fats; reading 
                         the labels helps to identify the sources.</li>
                     <li>Eating fish 2-3 times a week, with at least one serving of oily fish, 
                         will contribute to our right intake of unsaturated fats.</li>
                     <li>When cooking, we should boil, steam or bake, rather than frying, 
                         remove the fatty part of meat, use vegetable oils.</li>
                 </ul>
               
                </p>

                <!--4 th-->
               <p>
                <a name="4"></a>
               </p>
               <h3 >4.Enjoy plenty of fruits and vegetables</h3>
               <br>
               <img src="<?php echo base_url('image/fruitsvegs2_1.png')?>" width="400px" height="200px">
               <br>
               <br>
               <p class="details">Fruits and vegetables are among the most important foods for giving 
                   us enough vitamins, minerals and fibre. We should try to eat at least 5 servings a day. 
                   For example, a glass of fresh fruit juice at breakfast, perhaps an apple and a piece 
                   of watermelon as snacks, and a good portion of different vegetables at each meal.
               
                </p>

                <!--5 th-->
               <p>
                <a name="5"></a>
               </p>
               <h3 >5.Reduce salt and sugar intake</h3>
               <br>
               <img src="<?php echo base_url('image/how_much_sugar-01.png')?>" width="400px" height="200px">
               <br>
               <br>
               <p class="details">A high salt intake can result in high blood pressure, and increase the 
                   risk of cardiovascular disease. There are different ways to reduce salt in the diet:
                 <ul class="list">
                     <li>When shopping, we could choose products with lower sodium content.</li>
                     <li>When cooking, salt can be substituted with spices, increasing the 
                         variety of flavours and tastes.</li>
                     <li>When eating, it helps not to have salt at the table, or at least not to 
                         add salt before tasting.</li>
                 </ul>
                <p class="details">
                 Sugar provides sweetness and an attractive taste, but sugary foods and drinks are 
                 rich in energy, and are best enjoyed in moderation, as an occasional treat. 
                 We could use fruits instead, even to sweeten our foods and drinks.
                </p>
                </p>

                <!--6 th-->
               <p>
                <a name="6"></a>
               </p>
               <h3 >6.Eat regularly, control the portion size</h3>
               <br>
               <img src="<?php echo base_url('image/landing-whats-in-food.jpg')?>" width="400px" height="200px">
               <br>
               <br>
               <p class="details">Eating a variety of foods, regularly, and in the right amounts 
                       is the best formula for a healthy diet.

                       Skipping meals, especially breakfast, can lead to out-of-control hunger, 
                       often resulting in helpless overeating. Snacking between meals can help 
                       control hunger, but snacking should not replace proper meals. For snacks, 
                       we could choose yoghurt, a handful of fresh or dried fruits or vegetables 
                       (like carrot sticks), unsalted nuts, or perhaps some bread with cheese.
                
                        Paying attention to portion size will help us not to consume too much 
                        calories, and will allow us to eat all the foods we enjoy, without 
                        having to eliminate any.
                 <ul class="list">
                     <li>Cooking the right amount makes it easier to not overeat.</li>
                     <li>Some reasonable serving sizes are: 100 g of meat; one medium piece of fruit; 
                         half a cup of raw pasta.</li>
                     <li>Using smaller plates helps with smaller servings.</li>
                     <li>Packaged foods, with calorie values on the pack, could aid portion control.</li>
                     <li>If eating out, we could share a portion with a friend.</li>
                 </ul>
                 </p>

                 <!--7 th-->
                <p>
                  <a name="7"></a>
                </p>
                 <h3 >7.Drink plenty of fluids</h3>
                 <br>
                 <img src="<?php echo base_url('image/drinks.jpg')?>" width="400px" height="200px">
                 <br>
                 <br>
                 <p class="details">Adults need to drink at least 1.5 litres of fluid a day! Or more if 
                   it's very hot or they are physically active. Water is the best source, of course, 
                   and we can use tap or mineral water, sparkling or non-sparkling, plain or flavoured. 
                   Fruit juices, tea, soft drinks, milk and other drinks, can all be okay - from time to time.
               
                 </p>

                 <!--8 th-->
               <p>
                <a name="8"></a>
               </p>
               <h3 >8.Maintain a healthy body weight</h3>
               <br>
               <img src="<?php echo base_url('image/weight.jpg')?>" width="400px" height="200px">
               <br>
               <br>
               <p class="details">The right weight for each us depends on factors like our gender, height, 
                   age, and genes. Being affected by obesity and overweight increases the risks of a wide 
                   range of diseases, including diabetes, heart diseases, and cancer.

                Excess body fat comes from eating more than we need. The extra calories can come from 
                any caloric nutrient - protein, fat, carbohydrate, or alcohol, but fat is the most 
                concentrated source of energy. Physical activity helps us spend the energy, and makes 
                us feel good. The message is reasonably simple: if we are gaining weight, we need to 
                eat less and be more active!
                </p>
               
                <!--9 th-->
               <p>
                <a name="9"></a>
               </p>
               <h3 >9.Get on the move, make it a habit!</h3>
               <br>
               <img src="<?php echo base_url('image/running.jpg')?>" width="400px" height="200px">
               <br>
               <br>
               <p class="details">Physical activity is important for people of all weight ranges 
                   and health conditions. It helps us burn off the extra calories, it is good for 
                   the heart and circulatory system, it maintains or increases our muscle mass, 
                   it helps us focus, and improves overall health well-being. We don't have to be 
                   top athletes to get on the move! 150 minutes per week of moderate physical 
                   activity is advised, and it can easily become part of our daily routine. We all could:
                 <ul class="list">
                     <li>use the stairs instead of the elevator,</li>
                     <li>go for a walk during lunch breaks (and stretch in our offices in between)</li>
                     <li>make time for a family weekend activity</li>
                 </ul>
                 </p>

                 <!--10 th-->
               <p>
                <a name="10"></a>
               </p>
               <h3 >10.Start now! And keep changing gradually.</h3>
               <br>
               <img src="<?php echo base_url('image/download.jfif')?>" width="400px" height="200px">
               <br>
               <br>
               <p class="details">Gradual changes in our lifestyle are easier to maintain 
                   than major changes introduced all at once. For three days, we could write 
                   down the foods and drinks we consume throughout the day, and make a note of 
                   the amount of movement we made. It won’t be difficult to spot where we could improve:
                 <ul class="list">
                     <li>Skipping breakfast? A small bowl of muesli, a piece of bread or fruit, 
                         could help slowly introduce it into our routine</li>
                     <li>Too few fruits and vegetables? To start with, we can introduce one extra piece a day.</li>
                     <li>Favourite foods high in fat? Eliminating them abruptly could fire back, and 
                         make us return to the old habits. We can choose low fat options instead, 
                         eat them less frequently, and in smaller portions.</li>
                     <li>Too little activity? Using the stairs daily could be a great first move.</li>
                 </ul>
                 </p>

